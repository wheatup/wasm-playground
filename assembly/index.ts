export function add(a: i32, b: i32): i32 {
	return a + b;
}

export function subtract(a: i32, b: i32): i32 {
	return a - b;
}

export function multiply(a: i32, b: i32): i32 {
	return a * b;
}

export function divide(a: i32, b: i32): i32 {
	return a / b;
}

export function check_password(a: i32): boolean {
	return a === 1337;
}